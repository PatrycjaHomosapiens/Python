# pole kwadratu o boku 2 [cm]
a = 2
pole_kwadratu = a ** 2
print(pole_kwadratu)

# obwód trójkąta o bokach 3, 4, 6 [cm]
tr1 = 3
tr2 = 4
tr3 = 6
obwod_trojkata = tr1 + tr2 + tr3
print(obwod_trojkata)

# pole koła o promeniu 3 [cm]
pi = 3.14
r = 3
pole_kola = pi * r ** 2
print(pole_kola)